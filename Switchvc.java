public class Switchvc{
        public static void main(String [] args){
            char ab='e';
            switch(ab){
    case 'a':
       System.out.println("Vowel");
       break;
    case 'e':
        System.out.println("Vowel");
        break;
    case 'i':
         System.out.println("Vowel");
         break;
    case 'o':
         System.out.println("Vowel");
         break;
    case 'u':
         System.out.println("Vowel");
         break;
     default:
          System.out.println("Consonant");
}
}
}